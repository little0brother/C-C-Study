/******************取指定位****************************/
/* 取一个char a 从右端开始的2~5位                      */
/* 1.先使a右移2位：a>>2 目的是使要取出的那几位移到最右端 */
/* 2.设置一个低4位全为1，其余全为0的数 ~（~0<<4)        */
/* 3.将上面1.2. 进行&运算  （a>>4) & ~(0<<4)          */
/*****************************************************/

#include <stdio.h>

int main4()
{
	char a, b, c, d;
	printf("输入代检验数字：");
	scanf("%d", &a);
	b = a >> 2;
	c = ~(~0 << 4);
	d = b & c;

	printf("%d", d);

	return 0;
}